// mendefinisikan urutan angka
const nums = [2, 4, 5, 10];

// mengalikan angka 2 dan 10
let result = nums[0] * nums[3]; //2*10

// menambahkan angka 4 dan 5 ke dalam hasil perkalian
result += nums[1] + nums[2]; //4+5

// mengurangi hasil dengan selisih antara 29 dan 25
result -= result - 25;

// menampilkan nilai akhir
console.log(result);

